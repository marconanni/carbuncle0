    public void caricaRimanenze(Vector<Prodotto> prodotti, Vector<Integer> quantita)
    {
        int conta = 0;

        for (int i = 0; i < prodotti.size(); i++)
        {
            Prodotto prodotto = prodotti.get(i);
            
            if (quantita.get(i) > 0)
            {
                jPanelContainer.add(new RimanenzaSingola(prodotto.getMarca(), prodotto.getModello(), quantita.get(i)));
                conta++;
            }
        }

        this.setSize(((conta > 3)?3:conta)*((RimanenzaSingola)jPanelContainer.getComponent(0)).getIconWidth(), this.getHeight());
        jPanelContainer.updateUI();
        jPanelContainer.validate();
    }
