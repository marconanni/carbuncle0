    private void caricaProdotti()
    {
        jListProdotti.removeAll();
        if (!prodotti.isEmpty())
        {
            String s[] = new String[prodotti.size()];
            int j = 0;

            for (Iterator<Prodotto> it = prodotti.iterator(); it.hasNext();)
            {
                Prodotto prodotto = it.next();
                s[j] = prodotto.getMarca() + divisione + prodotto.getModello();
                j++;
            }

            jListProdotti.setListData(s);
        }
    }

    private void caricaGiacenza()
    {
        jListGiacenza.removeAll();
        if (giacenza.isEmpty())
            return;
        
        Enumeration<Prodotto> p = giacenza.keys();
        Collection<Integer> i = giacenza.values();
        String s[] = new String[i.size()];
        Prodotto pro = null;

        for (int j = 0; j < s.length; j++)
        {
            pro = p.nextElement();
            s[j] = pro.getMarca() + " " + pro.getModello() + ", " + i.toArray()[j];
        }

        jListGiacenza.setListData(s);
    }

