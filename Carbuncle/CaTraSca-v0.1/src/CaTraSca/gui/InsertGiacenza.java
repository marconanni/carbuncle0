    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int quantita = 0;

        if (!jTextFieldMarca.getText().equals("") && !jTextFieldModello.getText().equals(""))
        {
            String marca = jTextFieldMarca.getText();
            String modello = jTextFieldModello.getText();
            boolean flag = false;
            Prodotto p = null;
            try { quantita = Integer.parseInt(jTextFieldQuantita.getText()); }
            catch(Exception e) { setErrore("Errore nel valutare la quantita'."); }

            for (Iterator<Prodotto> it = prodotti.iterator(); it.hasNext();)
            {
                Prodotto prodotto = it.next();
                if (prodotto.getMarca().equals(marca) && prodotto.getModello().equals(modello))
                {
                    flag = true;
                    p = new Prodotto(prodotto.getMarca(), prodotto.getModello());
                    break;
                }
            }

            if (!flag)
            {
                p = new Prodotto(marca, modello);
                prodotti.add(p);
                caricaProdotti();
            }

            giacenza.put(p, quantita);
            caricaGiacenza();

            scaricaTexts();
            risultato = true;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

